/* =========================================================================
 * hangul.js — 한글 자모 획수 (한글 성명학용)
 * 한자가 없는 한글 전용 이름의 수리·음양 계산에 사용.
 * 전통 한글 획수표(훈민정음 기준) — 겹자모는 구성 자모 합.
 * ========================================================================= */
(function () {
  // 단일 자모 획수
  var S = {
    'ㄱ':2,'ㄴ':2,'ㄷ':3,'ㄹ':5,'ㅁ':4,'ㅂ':4,'ㅅ':2,'ㅇ':1,'ㅈ':3,'ㅊ':4,
    'ㅋ':3,'ㅌ':4,'ㅍ':4,'ㅎ':3,
    'ㅏ':2,'ㅑ':3,'ㅓ':2,'ㅕ':3,'ㅗ':2,'ㅛ':3,'ㅜ':2,'ㅠ':3,'ㅡ':1,'ㅣ':1,
    'ㅐ':3,'ㅒ':4,'ㅔ':3,'ㅖ':4
  };
  // 겹자모 = 구성 자모 합 (된소리·복합모음·겹받침)
  var COMPO = {
    'ㄲ':['ㄱ','ㄱ'],'ㄸ':['ㄷ','ㄷ'],'ㅃ':['ㅂ','ㅂ'],'ㅆ':['ㅅ','ㅅ'],'ㅉ':['ㅈ','ㅈ'],
    'ㅘ':['ㅗ','ㅏ'],'ㅙ':['ㅗ','ㅐ'],'ㅚ':['ㅗ','ㅣ'],'ㅝ':['ㅜ','ㅓ'],'ㅞ':['ㅜ','ㅔ'],
    'ㅟ':['ㅜ','ㅣ'],'ㅢ':['ㅡ','ㅣ'],
    'ㄳ':['ㄱ','ㅅ'],'ㄵ':['ㄴ','ㅈ'],'ㄶ':['ㄴ','ㅎ'],'ㄺ':['ㄹ','ㄱ'],'ㄻ':['ㄹ','ㅁ'],
    'ㄼ':['ㄹ','ㅂ'],'ㄽ':['ㄹ','ㅅ'],'ㄾ':['ㄹ','ㅌ'],'ㄿ':['ㄹ','ㅍ'],'ㅀ':['ㄹ','ㅎ'],'ㅄ':['ㅂ','ㅅ']
  };
  function jamoStroke(j) {
    if (S[j] != null) return S[j];
    if (COMPO[j]) return COMPO[j].reduce(function (a, x) { return a + (S[x] || 0); }, 0);
    return 0;
  }
  var CHO = ['ㄱ','ㄲ','ㄴ','ㄷ','ㄸ','ㄹ','ㅁ','ㅂ','ㅃ','ㅅ','ㅆ','ㅇ','ㅈ','ㅉ','ㅊ','ㅋ','ㅌ','ㅍ','ㅎ'];
  var JUNG = ['ㅏ','ㅐ','ㅑ','ㅒ','ㅓ','ㅔ','ㅕ','ㅖ','ㅗ','ㅘ','ㅙ','ㅚ','ㅛ','ㅜ','ㅝ','ㅞ','ㅟ','ㅠ','ㅡ','ㅢ','ㅣ'];
  var JONG = ['','ㄱ','ㄲ','ㄳ','ㄴ','ㄵ','ㄶ','ㄷ','ㄹ','ㄺ','ㄻ','ㄼ','ㄽ','ㄾ','ㄿ','ㅀ','ㅁ','ㅂ','ㅄ','ㅅ','ㅆ','ㅇ','ㅈ','ㅊ','ㅋ','ㅌ','ㅍ','ㅎ'];

  // 한글 음절 1자의 성명학 획수 (초성+중성+종성 자모 합)
  function syllableStroke(ch) {
    var code = ch.charCodeAt(0) - 0xac00;
    if (code < 0 || code > 11171) return 0; // 한글 음절 아님
    var cho = Math.floor(code / 588), jung = Math.floor((code % 588) / 28), jong = code % 28;
    return jamoStroke(CHO[cho]) + jamoStroke(JUNG[jung]) + (jong ? jamoStroke(JONG[jong]) : 0);
  }
  window.HangulStroke = { syllable: syllableStroke, jamo: jamoStroke };
})();
